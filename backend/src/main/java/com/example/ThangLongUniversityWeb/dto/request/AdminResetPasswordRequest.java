package com.example.ThangLongUniversityWeb.dto.request;

import io.swagger.v3.oas.annotations.media.Schema;
import jakarta.validation.constraints.NotBlank;
import jakarta.validation.constraints.Size;
import lombok.Data;

@Data
@Schema(description = "Yêu cầu Admin reset mật khẩu cho người dùng")
public class AdminResetPasswordRequest {

    @Schema(description = "Mật khẩu mới do Admin tự nhập", required = true, minLength = 6, maxLength = 100)
    @NotBlank(message = "Mật khẩu mới không được để trống")
    @Size(min = 6, max = 100, message = "Mật khẩu mới phải từ 6 đến 100 ký tự")
    private String newPassword;
}
